void OP_Amp_Init(void)
{
  OPM = 0;
  // P40, P41 switch to analog pin
  P4CON |= (1<<1)|(1<<0); 
  
  OPM = OPN_IN_SHORT | mskOPOEN | OPP_GND;
  // Gain 
  OPGS = 0; // x1
  
  OPM |= mskOPEN;
}


